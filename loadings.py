def file_loading(file):
    """
    Load a file
    :param file: list
    :return tab: list of tuples
    """
    tab = []

    for elt in range(1, len(file)):
        element = file[elt][:-1]  # deleting \n
        element = element.split(":")
        tab.append((element[0], element[1]))

    return tab
